const { Sequelize, DataTypes } = require("sequelize");
const sequelize = new Sequelize('myapp', 'postgres', '12345', {
    host: 'localhost',
    dialect: "postgres",/* one of 'mysql' | 'postgres' | 'sqlite' | 'mariadb' | 'mssql' | 'db2' | 'snowflake' | 'oracle' */
    define: {
        timestamps: false
    },
    pool: {
        max: 5,
        min: 0,
        acquire: 30000,
        idle: 10000
    }
});


